    private void editarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarBtnActionPerformed
        int filSel = tablaMenus.getSelectedRow();
        
        try {
            int menuID = (Integer) tablaMenus.getValueAt(filSel, 0);
            String prod = (String) tablaMenus.getValueAt(filSel, 1);
            String desc = (String) tablaMenus.getValueAt(filSel, 2);
            float prec = (Float) tablaMenus.getValueAt(filSel, 3);

            String id_str = Integer.toString(menuID);
            String prec_str = Float.toString(prec);

            idIn.setText(id_str);
            prodIn.setText(prod);
            descIn.setText(desc);
            precioIn.setText(prec_str);
        } catch (Exception e){
            JOptionPane.showMessageDialog(this, "Hubuo un problema al seleccionar un producto", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_editarBtnActionPerformed

    private void enviarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enviarBtnActionPerformed
        // logica para editar o crear
        int filSel = tablaMenus.getSelectedRow();
        int menuID = Integer.parseInt(idIn.getText());
        String prod = prodIn.getText();
        String desc = descIn.getText();
        float prec = Float.parseFloat(precioIn.getText());
        
        
        if(GM.getProducto(menuID) == null){
            //logica crear
            expresseatapp.menu MENU = new expresseatapp.menu(0, prod, desc, prec);
            GM.crearMenu(MENU);
            modelo.addRow(new Object [] {MENU.getMenuID(), MENU.getProducto(), MENU.getDesc(), MENU.getPrecio()});
            modelo.setRowCount(0);
            generarTabla();
            JOptionPane.showMessageDialog(null, "Producto ceado con exito");
        } else {
            //logica editar
            expresseatapp.menu MENU = new expresseatapp.menu(menuID, prod, desc, prec);
            GM.editarMenu(MENU);
            modelo.setValueAt(MENU.getMenuID(), filSel, 0);
            modelo.setValueAt(MENU.getProducto(), filSel, 1);
            modelo.setValueAt(MENU.getDesc(), filSel, 2);
            modelo.setValueAt(MENU.getPrecio(), filSel, 3);
            modelo.setRowCount(0);
            generarTabla();
            JOptionPane.showMessageDialog(null, MENU.getProducto() + " editado con exito");
        }
        idIn.setText("0");
        prodIn.setText("");
        descIn.setText("");
        precioIn.setText("");
        
        
        
        
        
    }//GEN-LAST:event_enviarBtnActionPerformed

    private void elimBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_elimBtnActionPerformed
        // logica eliminar
        int filSel = tablaMenus.getSelectedRow();
        int idSel = (Integer) modelo.getValueAt(filSel, 0);
        String prod = (String) modelo.getValueAt(filSel, 1);

        try {
            int conf = JOptionPane.showInternalConfirmDialog(null, "Seguro que quieres eliminar: " + prod, " ", JOptionPane.YES_NO_OPTION);
            if(conf == JOptionPane.YES_OPTION){
                boolean borro = GM.eliProducto(idSel);
                if (borro){
                    modelo.removeRow(filSel);
                }
            }
        } catch (Exception e){
            JOptionPane.showMessageDialog(this, "Hubo un error al eliminar!", "ERROR", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_elimBtnActionPerformed

