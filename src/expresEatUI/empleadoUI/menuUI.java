    private int rowI = 0;
    
    
    
    public menuUI() {
        initComponents();
        
        List<expresseatapp.menu> menus = MENU.getMenus();
        for(expresseatapp.menu Menus : menus){
            id = Menus.getMenuID();
            producto = Menus.getProducto();
            desc = Menus.getDesc();
            precio = Menus.getPrecio();
            jTable1.setValueAt(id, rowI, 0);
            jTable1.setValueAt(producto, rowI, 1);
            jTable1.setValueAt(desc, rowI, 2);
            jTable1.setValueAt(precio, rowI, 3);
            rowI++;
        }

        inicio.getDato();
        expresseatapp.icono.establecerIcono(this); 
    }
